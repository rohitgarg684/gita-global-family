#!/usr/bin/env python3
"""Repair mechanical corruption in Hindi text inside data files.

ACCURACY-FIRST DESIGN.

The Hindi text in src/data/qa-bhagavad-dharma.ts (and a few other files) was
extracted from a PDF/Word source with a buggy pipeline that produced two kinds
of artifacts:

  A. Doubled vowel-signs (matras) -- e.g. मुुख, अग्नििu, कुुछ.
     These are NEVER legal in Hindi orthography. A consonant can carry at most
     one matra. Safe to collapse mechanically.

  B. Doubled trailing consonants after a halant -- e.g. अर्थथ, कर्मम, स्वर्गग.
     Here the corruption duplicated the second consonant of a half-form pair.
     UNSAFE TO GENERALIZE: legitimate Hindi words do have सीधा consonant
     repetition after halant. Examples that MUST NOT be touched:
       अध्ययन  (adhyayan, "study")        — ध्+य+य+न  legitimate
       व्यय     (vyay, "expense")           — व्+य+य    legitimate
       रिक्तता  (riktata, "emptiness")      — क्+त+त+ा  legitimate (ता suffix)
       तत्त्व   (tattva, "essence")          — त्+त+्+व  legitimate
       सत्ता    (satta, "power")             — त्+त+ा    legitimate
     A blanket regex was experimentally shown to mangle these.

DECISIONS:

  * Apply (A) automatically -- mechanical, lossless, never wrong.
  * For (B), use an explicit allowlist of known-corrupt -> known-correct
    mappings, mined and confirmed by hand. Anything not on the allowlist is
    left untouched (and reported for human review). This is far smaller than
    "fix everything via regex" but it is correct.
  * Replacement-char (U+FFFD) cases are extracted to a review file. Cannot be
    auto-fixed -- information is lost.

Usage:
  python3 scripts/fix-hindi-corruption.py [--dry-run]
"""

