# Compile allowlist into a single alternation regex (longest first to avoid
# prefix collisions).
_keys_sorted = sorted(HALANT_DOUBLING_ALLOWLIST.keys(), key=len, reverse=True)
ALLOWLIST_RE = re.compile("|".join(re.escape(k) for k in _keys_sorted))


# ---------------------------------------------------------------------------
# Phase C: just count remaining `(C)्\1\1+` patterns so we can report what is
# left for human review.
# ---------------------------------------------------------------------------
CONS = r"[\u0915-\u0939\u0958-\u095F]"
REMAINING_HALANT_DOUBLING_RE = re.compile(rf"\w*{CONS}{HALANT}({CONS})\1+\w*")


def apply_always_illegal(text: str) -> tuple[str, int]:
    total = 0
    for pat, repl in ALWAYS_ILLEGAL_DOUBLES:
        text, n = pat.subn(repl, text)
        total += n
    return text, total


def apply_allowlist(text: str) -> tuple[str, int]:
    """Apply word-level allowlist replacements."""
    n = 0

    def _repl(m: re.Match) -> str:
        nonlocal n
        n += 1
        return HALANT_DOUBLING_ALLOWLIST[m.group(0)]

    new_text = ALLOWLIST_RE.sub(_repl, text)
    return new_text, n


FFFD = "\uFFFD"


def collect_fffd(text: str, rel_path: str) -> list[str]:
    out = []
    for ln_idx, line in enumerate(text.split("\n"), 1):
        if FFFD not in line:
            continue
        for m in re.finditer(re.escape(FFFD), line):
            i = m.start()
            left = line[max(0, i - 35) : i]
            right = line[i + 1 : i + 36]
            out.append(f"{rel_path}:{ln_idx}: ...{left}[\uFFFD]{right}...")
    return out


def collect_remaining_doublings(text: str, rel_path: str) -> Counter:
    """Find every (C)्CC+ token still present (i.e. NOT on the allowlist).

    Returns Counter of token -> count, so the human reviewer can decide which
    are real Hindi words (e.g., अध्ययन) and which are new corruption forms to
    add to the allowlist.
    """
    counts: Counter = Counter()
    # Tokenize on whitespace and TS-quote chars
    for token in re.findall(r"[\w\u0900-\u097F]+", text):
        if REMAINING_HALANT_DOUBLING_RE.fullmatch(token):
            counts[token] += 1
    return counts


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--dry-run", action="store_true")
    args = ap.parse_args()

    all_fffd: list[str] = []
    all_remaining: Counter = Counter()
    grand_a = 0
    grand_b = 0

    for rel in TARGET_FILES:
        p = ROOT / rel
        if not p.exists():
            print(f"SKIP (missing): {rel}")
            continue
        text = p.read_text(encoding="utf-8")

        all_fffd.extend(collect_fffd(text, rel))

        text_a, n_a = apply_always_illegal(text)
        text_ab, n_b = apply_allowlist(text_a)
        grand_a += n_a
        grand_b += n_b

        # Count remaining suspicious tokens AFTER our edits
        remaining = collect_remaining_doublings(text_ab, rel)
        for tok, c in remaining.items():
            all_remaining[(rel, tok)] += c

        print(f"  {rel}: matra fixes={n_a}, allowlist fixes={n_b}")

        if not args.dry_run and (n_a or n_b):
            p.write_text(text_ab, encoding="utf-8")

    # FFFD report
    if all_fffd:
        header = (
            f"# U+FFFD (replacement char) occurrences requiring manual review\n"
            f"# Total: {len(all_fffd)}\n"
            f"# Each line: file:lineno: ...<left>[\uFFFD]<right>...\n"
            f"# These cannot be auto-fixed; the lost character must be inferred\n"
            f"# from the original Hindi source.\n\n"
        )
        body = "\n".join(all_fffd) + "\n"
        if args.dry_run:
            print(
                f"\n(dry-run) Would write {len(all_fffd)} FFFD entries to {REVIEW_FILE.relative_to(ROOT)}"
            )
        else:
            REVIEW_FILE.write_text(header + body, encoding="utf-8")
            print(f"\nWrote {len(all_fffd)} FFFD entries -> {REVIEW_FILE.relative_to(ROOT)}")

    # Remaining-doubling report (tokens NOT auto-fixed; reviewer should decide
    # which are real Hindi and which to add to the allowlist).
    if all_remaining:
        # Aggregate by token across files
        token_totals: Counter = Counter()
        token_files: dict[str, set[str]] = {}
        for (rel, tok), c in all_remaining.items():
            token_totals[tok] += c
            token_files.setdefault(tok, set()).add(rel)

        lines = [
            "# Tokens matching pattern (C)्CC+ that were NOT auto-fixed.",
            "# REVIEWER: for each token, decide:",
            "#   - Real Hindi word (e.g. अध्ययन, व्यय, रिक्तता, तत्त्व, सत्ता): leave as-is.",
            "#   - Corruption (e.g. कर्मम, अर्थथ): add to HALANT_DOUBLING_ALLOWLIST in",
            "#     scripts/fix-hindi-corruption.py and re-run.",
            "# Format: <count>\t<token>\t<files>",
            "",
        ]
        for tok, cnt in token_totals.most_common():
            files_str = ", ".join(sorted(token_files[tok]))
            lines.append(f"{cnt}\t{tok}\t{files_str}")

        report = "\n".join(lines) + "\n"
        if args.dry_run:
            print(
                f"(dry-run) Would write {len(token_totals)} distinct unhandled tokens to {HALANT_REVIEW_FILE.relative_to(ROOT)}"
            )
        else:
            HALANT_REVIEW_FILE.write_text(report, encoding="utf-8")
            print(
                f"Wrote {len(token_totals)} distinct unhandled tokens -> {HALANT_REVIEW_FILE.relative_to(ROOT)}"
            )

    mode = "would apply" if args.dry_run else "applied"
    print()
    print(f"Total matra fixes {mode}: {grand_a}")
    print(f"Total allowlist fixes {mode}: {grand_b}")
    print(f"Total U+FFFD flagged for review: {len(all_fffd)}")
    print(f"Total unhandled doubling tokens flagged for review: {len(set(t for (_, t) in all_remaining))}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
